#include "Zombie.hpp"

Zombie	*zombieHorde(int N, std::string name)
{
	Zombie	*horde;

	horde = NULL;
	if (N)
		horde = new Zombie[N];
	for (int i(0); horde && i < N; i++)
		horde[i] = Zombie(name);
	return (horde);
}
